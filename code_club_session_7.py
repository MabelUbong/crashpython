ages = {'Chris': 20, 'Meg': 23}

"""Loop through a dictionary by key"""

for name in ages.keys():
    print(name)

"""Loop through a dictionary by value"""

for age in ages.values():
    print(age)

"""Loop through a dictionary be item"""

for name, age in ages.items():
    print(name, "is", age)

